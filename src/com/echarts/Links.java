package com.echarts;

public class Links {
    private int source;
    private int target;
    private String name;

    public int getSource() {
        return source;
    }

    public void setSource(int i) {
        this.source = i;
    }

    public int getTarget() {
        return target;
    }

    public void setTarget(int target) {
        this.target = target;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

	}