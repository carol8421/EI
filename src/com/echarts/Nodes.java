package com.echarts;

public class Nodes {
    private String name;
    
    private int category;
   
	private int id;
    public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public int getCategory() {
		return category;
	}

	public void setCategory(int category) {
		this.category = category;
	}
}